                Department of Computer Science & Engineering was established in the year 2002. Annual intake of students for B.Tech Computer Science & Engineering course is 120 and M.Tech (CSIS) is 24. Department is also recognized by APJ Abdul Kalam Technological University as a research center for Ph.D programme. The department has competent and committed faculty which encourages students' involvement in various co-curricular activities along with their academics. Computer Science students consistently bagged top spots in the University Examinations. A large number of students from our department get placed every year in national and multinational companies. The department also takes initiative for the conduct of various inter/intra collegiate competitions to improve the skills of students. The students are also provided with the finest opportunities to join various professional societies like ACM, IEEE, CSI, etc. CSI student branch has consecutively won the best student branch award.<br>

